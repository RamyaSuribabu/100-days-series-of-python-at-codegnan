#printing numbers from 1 to 10 using for
for i in range(1, 11):
    print(i)
#printing the  numbers between 2 to 21 ,starting  with 2 and step size is 2
for i in range(2, 21, 2):
    print(i)
#printing the string
name = "Python"
for ch in name:
    print(ch)
#printing numbers from 1 to 10 using while    
i = 1
while i <= 10:
    print(i)
    i += 1
#printing odd numbers from 1 to 20 using while
i = 1
while i <= 20:
    print(i)
    i += 2
